@pytest.mark.parametrize(
    "seg_file",
    SEG_NRRD_FILES,
    ids=[f.name for f in SEG_NRRD_FILES],
)
def test_real_world_round_trip(seg_file: Path):
    """parse → serialize → re-parse should preserve segment structure."""
    header = nrrd.read_header(str(seg_file))
    keyvalues: dict[str, str] = {}
    for k, v in header.items():
        if not k.startswith(("type", "dimension", "space", "sizes", "encoding", "endian",
                             "kinds", "data file", "datafile", "content",
                             "sample units", "spacings", "thicknesses",
                             "axis m", "axism", "centerings", "labels", "units",
                             "min", "max", "old", "number", "line", "byte")):
            keyvalues[k] = str(v)

    ext1, _ = parse_seg_keyvalues(keyvalues)
    flat = serialize_seg_extension(ext1)
    ext2, remaining = parse_seg_keyvalues(flat)

    assert len(ext2.segments) == len(ext1.segments)
    assert [s.id for s in ext2.segments] == [s.id for s in ext1.segments]
    assert [s.name for s in ext2.segments] == [s.name for s in ext1.segments]
    assert [s.label_value for s in ext2.segments] == [s.label_value for s in ext1.segments]
    assert ext2.source_representation == ext1.source_representation
    assert not remaining  # no leaked keys


