@pytest.mark.parametrize(
    "seg_file",
    SEG_NRRD_FILES,
    ids=[f.name for f in SEG_NRRD_FILES],
)
def test_real_world_parses(seg_file: Path):
    """Each real-world .seg.nrrd file should parse without errors."""
    header = nrrd.read_header(str(seg_file))
    keyvalues: dict[str, str] = {}
    for k, v in header.items():
        if not k.startswith(("type", "dimension", "space", "sizes", "encoding", "endian",
                             "kinds", "data file", "datafile", "content",
                             "sample units", "spacings", "thicknesses",
                             "axis m", "axism", "centerings", "labels", "units",
                             "min", "max", "old", "number", "line", "byte")):
            keyvalues[k] = str(v)

    ext, remaining = parse_seg_keyvalues(keyvalues)
    assert ext is not None, f"No segmentation found in {seg_file.name}"
    assert len(ext.segments) > 0

    # Validate model round-trip
    dumped = ext.model_dump(exclude_none=True)
    reloaded = SegmentationExtension(**dumped)
    assert len(reloaded.segments) == len(ext.segments)


