def test_tags_and_terminology():
    """Tags including TerminologyEntry should parse into dicom/designations/tags."""
    kv = {
        "Segment0_ID": "S1",
        "Segment0_LabelValue": "1",
        "Segment0_Tags": (
            "Segmentation.Status:inprogress|"
            "TerminologyEntry:Segmentation category and type - 3D Slicer General Anatomy list"
            "~SCT^85756007^Tissue~SCT^85756007^Tissue~^^"
            "~Anatomic codes - DICOM master list~^^~^^|"
        ),
    }
    ext, _ = parse_seg_keyvalues(kv)
    seg = ext.segments[0]

    # Tags (non-terminology)
    assert seg.tags == {"Status": "inprogress"}

    # DICOM classification
    assert seg.dicom is not None
    assert seg.dicom.category.scheme == "SCT"
    assert seg.dicom.category.code == "85756007"
    assert seg.dicom.category.meaning == "Tissue"
    assert seg.dicom.type.code == "85756007"

    # Designation from type code
    assert seg.designations is not None
    assert len(seg.designations) == 1
    assert seg.designations[0].scheme == "SCT"
    assert seg.designations[0].code == "85756007"

    # Terminology registry
    assert ext.terminologies is not None
    assert "SCT" in ext.terminologies
    assert ext.terminologies["SCT"].name == "SNOMED Clinical Terms"


